package com.codekutter.genesis.pipelines.extensions;

/**
 * Email Operations enumerations.
 */
public enum EEmailOperations {
    /**
     * Send email.
     */
    Send,
    /**
     * Receive email.
     */
    Receive,
    /**
     * Mark email as read.
     */
    MarkAsRead,
    /**
     * Mark email as unread.
     */
    MarkAsUnread,
    /**
     * Delete the email.
     */
    Delete,
    /**
     * Mark email as answered.
     */
    Answered
}
